# Flow control statements
# 1 Conditional operators: == , != , < , > ,<= , >=
# 2 Boolean operators: and , or , not 
# 3 Boolean values: True False

crap = 5
print(crap == 5)
print(crap != 5)
print(crap < 6)
print(crap <= 10)


bag = 'stuff'
print(crap == 5 and bag == 'stuff')
print(crap == 4 or bag == 'stuff')

print('Nooooooooooooooooooooo!!')
print('Not!')
print(not False)
